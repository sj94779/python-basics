x = 1
y = 2.5
z = 3j

# convert from int to float
a = float(x)

# convert from float to int
b = int(y)

# convert from int to complex
c = complex(x)

print("x = " , x)
print("y = " , y)
print("z = " , z)

print("a = " , a)
print("b = " , b)
print("c = " , c)


